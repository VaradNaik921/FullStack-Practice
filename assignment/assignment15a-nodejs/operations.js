//Function to add
let add = (a, b) => {
    return a + b;
};

//Function to subtract
let sub = (a, b) => {
    return a - b;
};

//Function to multiply
let mul = (a, b) => {
    return a * b;
};

//Function to divide
let div = (a, b) => {
    return a / b;
};

//Exporting functions to use in main
module.exports = { add, sub, mul, div };